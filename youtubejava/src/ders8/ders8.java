package ders8;

public class ders8 {

	public static void main(String[] args) 
	{
		System.out.println("--------AND------------");
		System.out.println((8==8)&&(10==10));
		System.out.println((8==0)&&(10==10));
		System.out.println((8==8)&&(10==1));
		System.out.println((8==0)&&(10==1));
		
		
		System.out.println("--------OR------------");
		System.out.println((8==8)||(10==10));
		System.out.println((8==0)||(10==10));
		System.out.println((8==8)||(10==1));
		System.out.println((8==0)||(10==1));
		
	}

}
